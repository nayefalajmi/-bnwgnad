begin;

-- تمرية بن وقناد — بلا "معتّق"
update products set
  "Description" = 'تمرٌ فاخرٌ طازجٌ منتقى بعناية، طريّ القوام غنيّ المذاق، يُقدَّم بلمسة بن وقناد الأصيلة ليكون رفيق فناجين القهوة ولحظات الكرم.'
where id = 74;

-- رنجينة بن وقناد — على مكوّناتنا الفعلية (رطب سكري، جوز، هردة، طحينة، زعفران، هيل)
update products set
  "Description" = 'رنجينة بن وقناد؛ وصفةٌ كويتيةٌ تراثيةٌ طوّرناها بلمستنا الخاصة — رطبُ السكري الطريّ مع الجوز والطحينة والهردة، معطّرةٌ بالزعفران والهيل. مزيجٌ غنيٌّ متوازنٌ بين الحلاوة والنكهة العطرية الدافئة، يليق بأرقى الضيافة.'
where id = 81;

-- قهوة رحيق — بلا أي إشارة لبلد
update products set
  "Description" = 'قهوة رحيق؛ تحميصةٌ غنيةٌ بنكهةٍ عميقةٍ ورائحةٍ آسرةٍ، من حبوبٍ منتقاةٍ بعنايةٍ ومحمّصةٍ بإتقان. تجربةٌ عطريةٌ متكاملةٌ تمنح فنجانك حضوراً مميزاً في كل رشفة.'
where id = 121;

commit;
