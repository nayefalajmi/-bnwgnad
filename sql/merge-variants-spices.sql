-- ════════════════════════════════════════════════════════════════
--  دمج المنتجات المتشابهة (حب / مطحون) كمتغيرات
--
--  نُبقي منتجاً واحداً بالاسم الأساسي ونضيف عمود variants (حب/مطحون)،
--  ثم نحذف المنتج المكرّر. السعر يتغيّر تلقائياً في المتجر حسب الاختيار،
--  وقابل للتعديل لاحقاً من لوحة التحكم (نافذة تعديل المنتج).
--
--  ▸ القسم (1) مطلوب (اقط) بالأسعار التي حدّدتها.
--  ▸ الأقسام (2)-(4) أزواج حب/مطحون واضحة بنفس السعر — احذف أي قسم لا تريده.
--
--  شغّله مرة واحدة في: Supabase Dashboard ▸ SQL Editor ▸ Run.
-- ════════════════════════════════════════════════════════════════

begin;

-- (1) اقط  →  حب 2.000 / مطحون 2.500   [مطلوب]
update products set
  "Name"  = 'اقط',
  "Price" = 2.000,
  variants = '[{"label":"حب","price":2.0},{"label":"مطحون","price":2.5}]'::jsonb
where id = 16;
delete from products where id = 17;     -- اقط مطحون (دُمج كمتغيّر)

-- (2) فلفل أسود  →  حب / مطحون (1.500 لكليهما)
update products set
  "Name"  = 'فلفل أسود',
  "Price" = 1.500,
  variants = '[{"label":"حب","price":1.5},{"label":"مطحون","price":1.5}]'::jsonb
where id = 40;                            -- فلفل اسود حب
delete from products where id = 41;       -- فلفل اسود مطحون

-- (3) كزبرة  →  حب / مطحون (1.000 لكليهما)
update products set
  "Name"  = 'كزبرة',
  "Price" = 1.000,
  variants = '[{"label":"حب","price":1.0},{"label":"مطحون","price":1.0}]'::jsonb
where id = 43;                            -- كزبرة حب
delete from products where id = 44;       -- كزبرة مطحون

-- (4) كمون  →  حب / مطحون (1.000 لكليهما)
update products set
  "Name"  = 'كمون',
  "Price" = 1.000,
  variants = '[{"label":"حب","price":1.0},{"label":"مطحون","price":1.0}]'::jsonb
where id = 45;                            -- كمون حب
delete from products where id = 46;       -- كمون مطحون

commit;
